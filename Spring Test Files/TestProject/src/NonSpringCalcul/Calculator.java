package NonSpringCalcul;

public class Calculator {
	public void deohagi(int a, int b) {
		System.out.println(a + b);
	}
	public void bbaegi(int a, int b) {
		System.out.println(a - b);
	}
	public void gophagi(int a, int b) {
		System.out.println(a * b);
	}
	public void nanugi(int a, int b) {
		System.out.println(a / b);
	}
}
